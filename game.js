    const scale = Math.min(availableWidth / baseWidth, availableHeight / baseHeight);
    // å¤§ãã„ç”»é¢ã§ã¯ 1 ä»¥ä¸Šã®æ‹¡å¤§ã‚‚è¨±å¯ï¼ˆæ¥µå°ç”»é¢å‘ã‘ã«æœ€å°å€¤ã¯ç¶­æŒï¼‰
